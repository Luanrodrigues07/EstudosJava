package vetores3;

public class pessoas {

    private String nome;
    private double peso;

    public pessoas(String nome, double peso){
        this.nome = nome;
        this.peso = peso;
    }

    public String getNome(){
        return nome;
    }
    public String setNome(){
        return this.nome;
    }
    public double getPeso(){
        return peso;
    }
    public double setPeso(){
        return this.peso;
    }



 
} 