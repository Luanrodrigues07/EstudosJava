 package vetores2;

 public class products{
    private String name;
    private double price;

    public products(String name, double price){
        this.name = name;
        this.price = price;
    }
    public String getName() {
        return name;
    }
    public String setName(){
        return this.name;
    }
    public double getPrice(){
        return price;
    }
    public double setPrice(){
        return this.price;
    }

 }

